package org.aktin.importer.enums;

/**
 * possible keys for extern processing scripts
 */
public enum ScriptKey {
    /**
     * Unique script id
     */
    ID,
    /**
     * Name of script to show in view
     */
    VIEWNAME,
    /**
     * Script version
     */
    VERSION,
    /**
     * Http media type this script can process
     */
    MIMETYPE
}
