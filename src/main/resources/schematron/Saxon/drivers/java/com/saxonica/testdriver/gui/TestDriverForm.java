    public Element getCacheElement(String name) {
        ElementFilter filter=new org.jdom2.filter.ElementFilter(name);
        IteratorIterable<Element> it = cacheNode.getDescendants(filter);
        if (it.hasNext()) {
            return it.next();
        } else {
            return null;
        }
    }

