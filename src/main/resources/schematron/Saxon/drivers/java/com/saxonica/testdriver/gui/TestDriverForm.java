    private void checkCacheFileCreated() {
        cacheFile = new File("cache.xml");

        SAXBuilder builder = new SAXBuilder();
        try {
            cacheNode = builder.build(cacheFile);
            return;
        } catch (JDOMException e) {
            //
        } catch (IOException e) {
            //
        }

        String cacheStr = "<cache>\n" +
                "    <dir>\n" +
                "        <xsltTestDriver>\n" +
                "            <workingDir/>\n" +
                "            <resultsDir />\n" +
                "        </xsltTestDriver>\n" +
                "        <xslt30TestDriver>" +
                "            <workingDir/>\n" +
                "            <resultsDir />\n" +
                "        </xslt30TestDriver>\n" +
                "        <XMarkBenchmarkTestDriver>" +
                "            <workingDir/>\n" +
                "            <resultsDir />\n" +
                "        </XMarkBenchmarkTestDriver>\n" +
                "        <fotsTestDriver>" +
                "            <workingDir/>\n" +
                "            <resultsDir />\n" +
                "        </fotsTestDriver>\n" +
                "        <schemaTestDriver>" +
                "            <workingDir/>\n" +
                "            <resultsDir />\n" +
                "        </schemaTestDriver>\n" +
                "        <updateTestDriver>" +
                "            <workingDir/>\n" +
                "            <resultsDir />\n" +
                "        </updateTestDriver>\n" +
                "    </dir>\n" +
                "</cache>";
        try {
            cacheNode = builder.build(new StringReader(cacheStr));
        } catch (JDOMException e) {
            e.printStackTrace();
            throw new AssertionError(e);
        } catch (IOException e) {
            e.printStackTrace();
            throw new AssertionError(e);
        }
        org.jdom2.output.XMLOutputter out = new XMLOutputter();
        try {
            FileOutputStream os = new FileOutputStream(cacheFile);
            out.output(cacheNode, os);
            os.close();
        } catch (IOException err) {
            err.printStackTrace();
            throw new AssertionError(err);
        }

    }


